def add(a,b):
	c=a+b
	print(c)

def subtract(c,d):
	d=c-d
	print(d)

def multiply(e,f):
	f=e*f
	print(f)

def division(g,h):
	z=g/h
	print(z)

def modulus(j,k):
	y=j%k
	print(y)

def exponent(m,n):
	f=m**n
	print(f)

def whole_number_division(g,h):
	a=g//h
	print(a)

def percentage(h,k):
	s=(h/k)*100
	print(s)
